    public int saveGrnNloadGrnCash() {
        try {
            st = new StockControler();

            //        System.out.println(findSupIdByName.getAdressLine2());
          grnsaveid = st.grnsave(findSupIdByName, grndate);

            if (grnsaveid != 0) {
                pojo.Grn grnid = new m.Grn().getBy(grnsaveid);
                int grnlogsave = st.grnlogsave(grnid, findPridByName, txtbuyingprice, txtquantity, total);
                if (grnlogsave != 0) {
                    Errormzg.displayconfirmMessage("Stock added");
                    int i = st.getaverageprice(findPridByName);
                    if (i != 0) {
                        pojo.GrnLog grnlgid = new m.GrnLog().getById(grnlogsave);
                        this.grnid = grnid;
                        this.grnlgid = grnlgid;
                        return i;
                    } else {
                        Errormzg.displayerrorMessage("unable to Update Products");
                        return 0;
                    }
                } else {
                    Errormzg.displayerrorMessage("unable to add stock");
                    return 0;
                }
            } else {

                Errormzg.displayerrorMessage("Unable to save Grn");
                return 0;

            }
        } catch (Exception e) {
            e.printStackTrace();
            return 0;

        }

    }

