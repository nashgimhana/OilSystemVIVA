    private void txtchequeamountKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtchequeamountKeyPressed
        // TODO add your handling code here:
        try {
            double totamount = 0.0;

            DefaultTableModel defaultTableModel = (DefaultTableModel) tblchequedetails.getModel();
            if (evt.getKeyChar() == KeyEvent.VK_ENTER) {
                Vector v = new Vector();
                if (!txtchequeno.getText().trim().isEmpty()) {
                    if (!cmbbank.getSelectedItem().toString().isEmpty()) {
                        if (!txtchequeamount.getText().isEmpty()) {
                            v.add(txtchequeno.getText().toString());
                            v.add(cmbbank.getSelectedItem().toString());
                            double chequeammount = Double.parseDouble(txtchequeamount.getText());
                            chequeammount = (Math.round(chequeammount * 100.0) / 100.0);
                            v.add(chequeammount);
                            v.add(((JTextField) chkdate.getDateEditor().getUiComponent()).getText());
                            defaultTableModel.addRow(v);
                            try {
                                totamount = Double.parseDouble(txttototalamount.getText());
                            } catch (Exception e) {
                                totamount = 0.0;
                            }
//                System.out.println(totamount);
                            double fullamount = totamount + chequeammount;
                            txttototalamount.setText(String.valueOf(fullamount));
                            txtchequeno.setText("");
                            txtchequeamount.setText("");
                            cmbbank.setSelectedItem(null);
                            txtchequeno.requestFocus();
                        } else {
                            Errormzg.displayerrorMessage("Empty Filds");
                        }
                    } else {
                        Errormzg.displayerrorMessage("Empty Filds");
                    }
                } else {
                    Errormzg.displayerrorMessage("Empty Filds");
                }

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_txtchequeamountKeyPressed
    public int pay() {
        try {
            double totammount = Double.parseDouble(txttototalamount.getText());
            if (radiocash.isSelected() == true || radiocheque.isShowing() == true) {
                int saved = 0;
                if (!txttototalamount.getText().isEmpty()) {
                    if (radiocash.isSelected() == true) {
                        saved = new grncashcontroller().saveMoneyBook(1, totammount, grndate, crntvalue, supplier);
                    } else {
                        if (tblchequedetails.getRowCount() != 0) {
                            saved = new grncashcontroller().saveMoneyBook(2, totammount, grndate, crntvalue, supplier, tblchequedetails);
                        } else {
                            Errormzg.displayerrorMessage("Please add cheques to table");
                        }

                    }
                    if (saved != 0) {
                        Errormzg.displayconfirmMessage("Payment Recorded");
                        new report.Psmodle().grn(grnsaveid);
                        this.dispose();
                    } else {
                        Errormzg.displayerrorMessage("somthing went wrong");
                    }
                } else {
                    Errormzg.displayerrorMessage("please set total");
                }
            } else {
                Errormzg.displayerrorMessage("please select cash type");
            }
            return 1;

        } catch (Exception e) {
            e.printStackTrace();
            return 0;
        }

    }

    private void btnpayMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnpayMouseReleased
        // TODO add your handling code here:

        if (radiocheque.isSelected() || radiocash.isSelected()) {
            if (!txttototalamount.getText().trim().isEmpty()) {
                double totammount = Double.parseDouble(txttototalamount.getText());
                double tot1 = Double.parseDouble(lblsupcredit.getText());
                double tot2 = Double.parseDouble(lbltotal.getText());
                if (tot1 >= 0) {
                    if (tot2 >= 0) {
                        if (totammount >= 0) {
                            if (tot1 + tot2 >= totammount) {
                                int done = saveGrnNloadGrnCash();
                                if (done != 0) {
                                    pay();
                                } else {
                                    Errormzg.displayerrorMessage("Unable to save stock");
                                }

                            } else {
                                Errormzg.displayerrorMessage("please enter less value");
                            }
                        }
                    }
                }
            } else {
                Errormzg.displayerrorMessage("Empty Fields");
            }
        } else {
            Errormzg.displayerrorMessage("please select pay method");
        }


    }//GEN-LAST:event_btnpayMouseReleased

