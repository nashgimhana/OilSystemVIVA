    private void tf_checkAmountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tf_checkAmountActionPerformed
        // TODO add your handling code here:

        try {
            double rcREDIT = Double.parseDouble(tf_routeCredit.getText());
            rcREDIT = (Math.round(rcREDIT * 100.0) / 100.0);
            double rcDebitPay = Double.parseDouble(tf_checkAmount.getText());
            rcDebitPay = (Math.round(rcDebitPay * 100.0) / 100.0);
            if (rcDebitPay > rcREDIT) {
                JOptionPane.showMessageDialog(rootPane, "Please enter a value less than or equal to Route Credit", "Debit Pay", JOptionPane.OK_OPTION);
            } else {
                if (cb_bankName.getSelectedIndex() != 0) {
                    new DebitCashTable().setDebitCashTable(tf_checkNumber, cb_bankName, tf_checkAmount, tbl_debitCheckList,dc_checkDate);
                    new DebitCashTable().tableTotal(tf_totalDebitPay, tbl_debitCheckList);
                } else {
                    Errormzg.displayerrorMessage("Please select a Bank");
                }

            }
//        tf_checkNumber.setText(null);
//        tf_checkAmount.setText(null);
//        cb_bankName.setSelectedIndex(0);
//        tf_checkNumber.requestFocus();
            tf_checkAmount.setText(null);
            tf_checkNumber.requestFocus();
            tf_checkNumber.setText(null);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_tf_checkAmountActionPerformed

    private void tf_checkAmountKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tf_checkAmountKeyTyped
        // TODO add your handling code here:
        char vChar = evt.getKeyChar();
        if (!(Character.isDigit(vChar)
                || (vChar == KeyEvent.VK_BACK_SPACE)
                || (vChar == KeyEvent.VK_DELETE) || vChar == KeyEvent.VK_PERIOD)) {
            evt.consume();
        }
    }//GEN-LAST:event_tf_checkAmountKeyTyped

