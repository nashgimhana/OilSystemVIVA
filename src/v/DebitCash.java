    private void btn_saverouteDebitMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_saverouteDebitMouseReleased
        // TODO add your handling code here:
        try {
            if (tf_totalDebitPay.getText().isEmpty()) {
                Errormzg.displayerrorMessage("Enter an amount");
            } else {
                double rcREDIT = Double.parseDouble(tf_routeCredit.getText());
                double rcDebitPay = Double.parseDouble(tf_totalDebitPay.getText());
                if (rcDebitPay > rcREDIT) {
                    JOptionPane.showMessageDialog(rootPane, "Please enter a value less than or equal to Route Credit", "Debit Pay", JOptionPane.OK_OPTION);
                } else {

                    new c.RouteDebitMoneyBook().routeDebitMoneyBookSave(cashType, dateChooserD, tf_totalDebitPay, routeReg, rootPane, tbl_debitCheckList, tf_checkNumber, cb_bankName, tf_checkAmount, dc_checkDate);
                    tf_routeCredit.setText(String.valueOf(routeReg.getCurrentCredit()));

                    JOptionPane.showMessageDialog(null, "Data Saved");

                    v.Delivery.getInstance().clearRouteDebit();
                    this.dispose();

                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }


    }//GEN-LAST:event_btn_saverouteDebitMouseReleased

