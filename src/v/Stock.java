    public int paycredit() {
        try {
            int saved = 0;
            if (radiocash.isSelected() == true || radiocheque.isShowing() == true) {

                if (!txtcrtot.getText().isEmpty()) {
                    if (radiocash.isSelected() == true) {
//                        saved = new grncashcontroller().saveMoneyBook(1, totammount, grndate, crntvalue, supplier);
                        saved = new c.StockControler().supplierpaycredit1(supidcredit, supcreditdate.getDate(), Double.parseDouble(lblsupliercrdit.getText()), Double.parseDouble(txtcrtot.getText()), 1);
                    } else {
                        if (tblcrcheque.getRowCount() != 0) {
//                            saved = new grncashcontroller().saveMoneyBook(2, totammount, grndate, crntvalue, supplier, tblchequedetails);
                            saved = new c.StockControler().supplierpaycredit2(supidcredit, supcreditdate.getDate(), Double.parseDouble(lblsupliercrdit.getText()), Double.parseDouble(txtcrtot.getText()), 2, tblcrcheque);
                        } else {
                            Errormzg.displayerrorMessage("Please add cheques to table");
                        }

                    }
                } else {
                    Errormzg.displayerrorMessage("please set total");
                }
            } else {
                Errormzg.displayerrorMessage("please select cash type");
            }
            return saved;

        } catch (Exception e) {
            e.printStackTrace();
            return 0;
        }

    }

